color_name = input("Введите название цвета: ")
if color_name == "красный":
    print("Код цвета RGB: (255, 0, 0)")
elif color_name == "оранжевый":
    print("Код цвета RGB: (255, 165, 0)")
elif color_name == "желтый":
    print("Код цвета RGB: (255, 255, 0)")
elif color_name == "зеленый":
    print("Код цвета RGB: (0, 128, 0)")
elif color_name == "синий":
    print("Код цвета RGB: (0, 0, 255)")
elif color_name == "фиолетовый":
    print("Код цвета RGB: (128, 0, 128)")
else:
    print("Этот цвет не распознан.")
